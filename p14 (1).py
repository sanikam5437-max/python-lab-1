
# 1e.py Bitwise operator
x=int(input("Enter first number:"))
y=int(input("Enter second number:"))
a1=x&y#Bitwise AND
a2=x|y#Bitwise OR
a3=~x#Bitwise NOT
a4=x^y#Bitwise XOR
a5=x>>2#Bitwise right shift
a6=x<<2#Bitwise Left shift
print("Bitwise AND=",a1)
print("Bitwise OR=",a2)
print("Bitwise XOR=",a3)
print("Bitwise NOT=",a4)
print("Bitwise AND=",a5)
print("Bitwise OR=",a6)
